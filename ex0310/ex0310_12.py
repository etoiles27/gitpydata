# 돈을 입력받아 5만원권, 만원권, 오천원권, 천원권으로 교환해서 출력하시오


money, b50000, b10000, b5000, b1000 = 0,0,0,0,0

money = int(input('교환할 돈을 입력하세요 : '))
print('입력한 금액 : {:,} 원'.format(money))


b50000 = money//50000
b10000 = (money%50000)//10000
b5000 = (money%50000%10000)//5000
b1000 = (money%50000%10000%5000)//1000

print('5만원 {} 개 \t만원 {} 개 \t5천원 {}개 \t천원 {}개'.format(b50000,b10000,b5000,b1000))